/**
 * <p>内容</p>
 * 2019/3/7/0007 13:28
 *
 * @author lvjie
 */
@SpringBootApplication
@EnableEurekaServer
public class EurekaServer {
    public static void main(String[] args) {
        SpringApplication.run(EurekaServer.class,args);
    }
}
